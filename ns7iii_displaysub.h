/* ── Low-level SysEx send (live updates only) ────────────────────────────
 * Builds the full Akai SysEx frame and writes it to the display handle.
 * Returns 0 on success, -1 on error or if handle is NULL. */
static int disp_send_sysex(snd_rawmidi_t *h, uint16_t *seq, uint8_t dev_id,
			   uint8_t cmd, const uint8_t *payload, int payload_len)
{
	if (!h || payload_len < 0)
		return -1;

	uint16_t msg_len = (uint16_t)(payload_len + 4);
	uint16_t exp_len = (uint16_t)(payload_len + 2);
	uint16_t s = (*seq)++;

	uint8_t hdr[12] = {
		0xF0,
		0x47,
		dev_id,
		0x37,
		0x05,
		cmd,
		(uint8_t)((msg_len >> 7) & 0x7F),
		(uint8_t)(msg_len & 0x7F),
		(uint8_t)((exp_len >> 7) & 0x7F),
		(uint8_t)(exp_len & 0x7F),
		(uint8_t)((s >> 7) & 0x7F),
		(uint8_t)(s & 0x7F),
	};
	uint8_t tail[2] = { 0xF7, 0xF7 };

	snd_rawmidi_write(h, hdr, 12);
	if (payload_len > 0)
		snd_rawmidi_write(h, payload, (size_t)payload_len);
	snd_rawmidi_write(h, tail, 2);
	snd_rawmidi_drain(h);
	return 0;
}

/* ── VDJ 0x60 session confirm ────────────────────────────────────────────
 * Sent once on the main MIDI output before opening the display ports.
 * No response expected; display accepts Akai SysEx immediately after. */
static void disp_send_confirm(void)
{
	static const uint8_t confirm[16] = { 0xf0, 0x00, 0x01, 0x3f, 0x7f, 0x37,
					     0x60, 0x00, 0x01, 0x49, 0x01, 0x00,
					     0x00, 0x00, 0x00, 0xf7 };
	if (g_midi_out)
		snd_rawmidi_write(g_midi_out, confirm, sizeof(confirm));
}

/* ── Full VDJ init sequence (50 messages per display) ───────────────────
 * Must be called once on connect before any live updates.
 * R=1 for Display Right (DISP_DEV_A=0x20), R=0 for Display Left (0x10).
 * Takes ~200ms on G4 due to USB latency. */
static void disp_send_init(snd_rawmidi_t *h, uint8_t dev_id)
{
	if (!h)
		return;
	int R = (dev_id == DISP_DEV_A); /* 1 = Right, 0 = Left */

	/* [01] sub=0x06 geometry */
	disp_raw(h, vdj_init_01, sizeof(vdj_init_01), dev_id);

	/* [02] sub=0x08 timing — byte[15]: 0x20 Left, 0x40 Right */
	{
		uint8_t buf[19];
		memcpy(buf, vdj_init_02, 19);
		buf[2] = dev_id;
		buf[15] = R ? 0x40 : 0x20;
		snd_rawmidi_write(h, buf, 19);
		snd_rawmidi_drain(h);
	}

	/* [03] sub=0x30 font atlas */
	disp_raw(h, vdj_init_03, sizeof(vdj_init_03), dev_id);

	/* [04-05] sub=0x01 glyph data — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_04, sizeof(vdj_init_04), dev_id,
		     (uint8_t)(vdj_init_04[10] + R));
	disp_raw_b10(h, vdj_init_05, sizeof(vdj_init_05), dev_id,
		     (uint8_t)(vdj_init_05[10] + R));

	/* [06-07] sub=0x02 text label — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_06, sizeof(vdj_init_06), dev_id,
		     (uint8_t)(vdj_init_06[10] + R));
	disp_raw_b10(h, vdj_init_07, sizeof(vdj_init_07), dev_id,
		     (uint8_t)(vdj_init_07[10] + R));

	/* [08-15] sub=0x07 bitmap tiles, group 0: byte[10]=0(L) or 1(R) */
	{
		uint8_t g0 = R ? 0x01 : 0x00;
		disp_tile07(h, dev_id, g0, 0x02, 0x20);
		disp_tile07(h, dev_id, g0, 0x04, 0x20);
		disp_tile07(h, dev_id, g0, 0x06, 0x20);
		disp_tile07(h, dev_id, g0, 0x08, 0x20);
		disp_tile07(h, dev_id, g0, 0x0a, 0x20);
		disp_tile07(h, dev_id, g0, 0x0c, 0x20);
		disp_tile07(h, dev_id, g0, 0x0e, 0x20);
		disp_tile07(h, dev_id, g0, 0x10, 0x10);
	}

	/* [16-23] sub=0x07 bitmap tiles, group 1: byte[10]=2(L) or 3(R) */
	{
		uint8_t g1 = R ? 0x03 : 0x02;
		disp_tile07(h, dev_id, g1, 0x02, 0x20);
		disp_tile07(h, dev_id, g1, 0x04, 0x20);
		disp_tile07(h, dev_id, g1, 0x06, 0x20);
		disp_tile07(h, dev_id, g1, 0x08, 0x20);
		disp_tile07(h, dev_id, g1, 0x0a, 0x20);
		disp_tile07(h, dev_id, g1, 0x0c, 0x20);
		disp_tile07(h, dev_id, g1, 0x0e, 0x20);
		disp_tile07(h, dev_id, g1, 0x10, 0x10);
	}

	/* [24-30] sub=0x24 image sprites — byte[10]=0-6, same for both displays */
	for (int i = 0; i < 7; i++)
		disp_sprite24(h, dev_id, (uint8_t)i);

	/* [31] sub=0x22 border */
	disp_raw(h, vdj_init_31, sizeof(vdj_init_31), dev_id);

	/* [32] sub=0x23 text config */
	disp_raw(h, vdj_init_32, sizeof(vdj_init_32), dev_id);

	/* [33-34] sub=0x05 meter — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_33, sizeof(vdj_init_33), dev_id,
		     (uint8_t)(vdj_init_33[10] + R));
	disp_raw_b10(h, vdj_init_34, sizeof(vdj_init_34), dev_id,
		     (uint8_t)(vdj_init_34[10] + R));

	/* [35-36] sub=0x04 UI widget — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_35, sizeof(vdj_init_35), dev_id,
		     (uint8_t)(vdj_init_35[10] + R));
	disp_raw_b10(h, vdj_init_36, sizeof(vdj_init_36), dev_id,
		     (uint8_t)(vdj_init_36[10] + R));

	/* [37-38] sub=0x10 layout — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_37, sizeof(vdj_init_37), dev_id,
		     (uint8_t)(vdj_init_37[10] + R));
	disp_raw_b10(h, vdj_init_38, sizeof(vdj_init_38), dev_id,
		     (uint8_t)(vdj_init_38[10] + R));

	/* [39-40] sub=0x03 palette — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_39, sizeof(vdj_init_39), dev_id,
		     (uint8_t)(vdj_init_39[10] + R));
	disp_raw_b10(h, vdj_init_40, sizeof(vdj_init_40), dev_id,
		     (uint8_t)(vdj_init_40[10] + R));

	/* [41] sub=0x0a capability — byte[10] += 1 for Right */
	disp_raw_b10(h, vdj_init_41, sizeof(vdj_init_41), dev_id,
		     (uint8_t)(vdj_init_41[10] + R));

	/* [42-49] sub=0x21 fill regions — byte[10]=0-7, same for both displays */
	disp_raw(h, vdj_init_42, sizeof(vdj_init_42), dev_id);
	disp_raw(h, vdj_init_43, sizeof(vdj_init_43), dev_id);
	disp_raw(h, vdj_init_44, sizeof(vdj_init_44), dev_id);
	disp_raw(h, vdj_init_45, sizeof(vdj_init_45), dev_id);
	disp_raw(h, vdj_init_46, sizeof(vdj_init_46), dev_id);
	disp_raw(h, vdj_init_47, sizeof(vdj_init_47), dev_id);
	disp_raw(h, vdj_init_48, sizeof(vdj_init_48), dev_id);
	disp_raw(h, vdj_init_49, sizeof(vdj_init_49), dev_id);

	/* [50] sub=0x20 clear */
	disp_raw(h, vdj_init_50, sizeof(vdj_init_50), dev_id);
}

/* ── BPM display update (sub=0x04) ─────────────────────────────────────
 * Sends the current track BPM to a display.
 * BPM is BCD-encoded at payload[15:16].  Template from pcap. */
static void disp_send_bpm(snd_rawmidi_t *h, uint16_t *seq, uint8_t dev_id,
			  float bpm)
{
	if (!h || bpm <= 0.0f)
		return;

	uint8_t
		pl
			[20] = {
				0x38, 0x01, 0x03, 0x00, 0x00, 0x00,
				0x00, 0x56, 0x40, 0x71, 0x02, 0x06,
				0x00, 0x00, 0x00, 0x00, /* pl[15] = BPM integer BCD  */
				0x00, /* pl[16] = BPM fraction BCD */
				0x00, 0x00, 0x00
			};

	int bpm_int = (int)bpm;
	int bpm_frac = (int)((bpm - (float)bpm_int) * 100.0f + 0.5f);
	pl[15] = (uint8_t)(((bpm_int / 10) << 4) | (bpm_int % 10));
	pl[16] = (uint8_t)(((bpm_frac / 10) << 4) | (bpm_frac % 10));

	disp_send_sysex(h, seq, dev_id, 0x04, pl, sizeof(pl));
}

/* ── Waveform send (sub=0x09 — RGB565 + 8→7 MIDI packing) ──────────────
 * Sends the full-track overview waveform to a display on track load.
 *
 * Encoding:
 *   1081 pixels × RGB565 LE (= 2162 real bytes + 1 pad = 2163 = 309×7)
 *   8→7 pack: each group of 7 real bytes → 8 MIDI bytes (bit 7 → msb byte)
 *   309 groups × 8 = 2472 MIDI bytes + 2 pad = 2474 in the message
 *
 * Message layout (2491 bytes total):
 *   [0-5]    f0 47 dev 37 05 09        header
 *   [6-7]    13 32                     total_len=2482 (14-bit: 19×128+50)
 *   [8-9]    13 30                     data_len=2480  (14-bit: 19×128+48)
 *   [10-11]  00 02                     flags (waveform display)
 *   [12-15]  00 00 00 00               scroll offset = track start
 *   [16..2489] 2474 bytes packed data
 *   [2490]   f7
 */
static void disp_send_waveform(snd_rawmidi_t *h, uint8_t dev_id, const Track *t)
{
	if (!h || !t->loaded || !t->wfm_low || t->wfm_bins == 0)
		return;

#define VDJ_WFM_PIXELS 1081
	static uint16_t pixels[VDJ_WFM_PIXELS];
	static uint8_t real[2163]; /* 309 × 7 = 2163 exactly */
	static uint8_t midi[2474]; /* 309 × 8 + 2 pad */
	static uint8_t msg[2491];

	/* Build RGB565 LE pixels from waveform bands */
	for (int px = 0; px < VDJ_WFM_PIXELS; px++) {
		uint32_t bin = (uint32_t)px * t->wfm_bins / VDJ_WFM_PIXELS;
		if (bin >= t->wfm_bins)
			bin = t->wfm_bins - 1;

		float lo = t->wfm_low[bin] / 255.0f;
		float mi = t->wfm_mid[bin] / 255.0f;
		float hi = t->wfm_high[bin] / 255.0f;

		float lo_n, mi_n, hi_n;
		wfm_normalize_bands(lo, mi, hi, &lo_n, &mi_n, &hi_n,
				    t->wfm_band_max);

		uint8_t R5 = (uint8_t)(lo_n * 31.0f + 0.5f);
		uint8_t G6 = (uint8_t)(mi_n * 63.0f + 0.5f);
		uint8_t B5 = (uint8_t)(hi_n * 31.0f + 0.5f);
		pixels[px] = (uint16_t)(((uint16_t)R5 << 11) |
					((uint16_t)G6 << 5) | B5);
	}

	/* Flatten to real bytes (RGB565 LE: low byte first) + zero pad */
	memset(real, 0, sizeof(real));
	for (int px = 0; px < VDJ_WFM_PIXELS; px++) {
		real[px * 2 + 0] = (uint8_t)(pixels[px] & 0xFFu);
		real[px * 2 + 1] = (uint8_t)(pixels[px] >> 8);
	}
	/* real[2162] = 0 pad to complete the 309th group of 7 */

	/* 8→7 MIDI packing: 7 real bytes → 8 MIDI bytes */
	memset(midi, 0, sizeof(midi));
	for (int g = 0; g < 309; g++) {
		const uint8_t *src = real + g * 7;
		uint8_t *dst = midi + g * 8;
		uint8_t msb = 0;
		for (int i = 0; i < 7; i++)
			if (src[i] & 0x80u)
				msb |= (uint8_t)(1u << i);
		dst[0] = msb;
		for (int i = 0; i < 7; i++)
			dst[1 + i] = src[i] & 0x7Fu;
	}
	/* midi[2472..2473] = 0 padding */

	/* Build sub=0x09 message */
	msg[0] = 0xf0;
	msg[1] = 0x47;
	msg[2] = dev_id;
	msg[3] = 0x37;
	msg[4] = 0x05;
	msg[5] = 0x09;
	msg[6] = 0x13;
	msg[7] = 0x32; /* total_len = 2482 */
	msg[8] = 0x13;
	msg[9] = 0x30; /* data_len  = 2480 */
	msg[10] = 0x00;
	msg[11] = 0x02; /* flags: waveform */
	msg[12] = 0x00;
	msg[13] = 0x00; /* scroll offset (start of track) */
	msg[14] = 0x00;
	msg[15] = 0x00;
	memcpy(msg + 16, midi, 2474);
	msg[2490] = 0xf7;

	snd_rawmidi_write(h, msg, sizeof(msg));
	snd_rawmidi_drain(h);
#undef VDJ_WFM_PIXELS
}

/* ── Position stream (sub=0x05) ──────────────────────────────────────────
 * Sends current playback position to the display at ~20Hz.
 * 16-byte payload per protocol capture. */
static void disp_send_position(snd_rawmidi_t *h, uint16_t *seq, uint8_t dev_id,
			       const Track *t, uint32_t beat_ctr,
			       uint16_t frame_addr)
{
	if (!h)
		return;

	uint8_t pl[16] = { 0 };

	/* Beat counter — 14-bit */
	pl[0] = (uint8_t)((beat_ctr >> 7) & 0x7Fu);
	pl[1] = (uint8_t)(beat_ctr & 0x7Fu);

	/* Frame address — 14-bit monotonic position */
	pl[4] = (uint8_t)((frame_addr >> 7) & 0x7Fu);
	pl[5] = (uint8_t)(frame_addr & 0x7Fu);

	/* Play flag */
	pl[10] = 0x00;
	pl[11] = t->playing ? 0x7D : 0x00; /* 0x7D=125 playing, 0 stopped */

	disp_send_sysex(h, seq, dev_id, 0x05, pl, sizeof(pl));
}

/* ── Display open/close ──────────────────────────────────────────────────
 * Auto-detects the NS7III display interfaces by ALSA card name.
 * Opens output-only handles (no handshake; VDJ protocol is send-only).
 * Sends the 0x60 session confirm on the main MIDI port first. */
static void disp_open(void)
{
	disp_send_confirm(); /* VDJ 0x60 confirm — sent once per session */

	snd_ctl_t *ctl;
	int card = -1;

	while (snd_card_next(&card) == 0 && card >= 0) {
		char ctl_name[32];
		snprintf(ctl_name, sizeof(ctl_name), "hw:%d", card);
		if (snd_ctl_open(&ctl, ctl_name, 0) < 0)
			continue;

		snd_ctl_card_info_t *info;
		snd_ctl_card_info_alloca(&info);
		snd_ctl_card_info(ctl, info);
		const char *name = snd_ctl_card_info_get_name(info);
		snd_ctl_close(ctl);

		if (!name)
			continue;

		char dev[32];
		snprintf(dev, sizeof(dev), "hw:%d,0,0", card);

		if (strstr(name, "Display Right") && !g_disp_a) {
			if (snd_rawmidi_open(NULL, &g_disp_a, dev,
					     SND_RAWMIDI_NONBLOCK) == 0)
				snprintf(g_fb_status, sizeof(g_fb_status),
					 "Display A: %s", dev);
		} else if (strstr(name, "Display Left") && !g_disp_b) {
			if (snd_rawmidi_open(NULL, &g_disp_b, dev,
					     SND_RAWMIDI_NONBLOCK) == 0)
				snprintf(g_fb_status, sizeof(g_fb_status),
					 "Display B: %s", dev);
		}
	}
}

static void disp_close(void)
{
	pthread_mutex_lock(&g_disp_mutex);
	if (g_disp_a) {
		snd_rawmidi_close(g_disp_a);
		g_disp_a = NULL;
	}
	if (g_disp_b) {
		snd_rawmidi_close(g_disp_b);
		g_disp_b = NULL;
	}
	pthread_mutex_unlock(&g_disp_mutex);
}

/* ── Display thread ──────────────────────────────────────────────────────
 * Runs at ~20Hz. Responsibilities:
 *   1. Send 50-message VDJ init sequence once on connect (first frame)
 *   2. Send full sub=0x09 waveform on track load (detected by filename change)
 *   3. Send sub=0x05 position stream every frame while playing
 *
 * The waveform send (~2.5KB USB transfer) is very fast under VDJ protocol. */
static void *display_thread(void *arg)
{
	(void)arg;

	char last_fn_a[MAX_FILENAME] = "";
	char last_fn_b[MAX_FILENAME] = "";
	int init_sent_a = 0;
	int init_sent_b = 0;

	uint32_t beat_ctr_a = 0;
	uint32_t beat_ctr_b = 0;
	uint16_t frame_addr = 0;

	const int period_us = 1000000 / 20; /* 20 Hz */

	while (g_running) {
		usleep((unsigned int)period_us);

		pthread_mutex_lock(&g_disp_mutex);

		/* ── Init on connect ── */
		if (g_disp_a && !init_sent_a) {
			disp_send_init(g_disp_a, DISP_DEV_A);
			init_sent_a = 1;
		}
		if (g_disp_b && !init_sent_b) {
			disp_send_init(g_disp_b, DISP_DEV_B);
			init_sent_b = 1;
		}

		/* ── Deck A waveform load ── */
		Track *ta = &g_tracks[0];
		Track *tb = &g_tracks[1];

		pthread_mutex_lock(&ta->lock);
		int a_loaded = ta->loaded;
		char a_fn[MAX_FILENAME];
		memcpy(a_fn, ta->filename, MAX_FILENAME);
		pthread_mutex_unlock(&ta->lock);

		if (g_disp_a && a_loaded && strcmp(a_fn, last_fn_a) != 0) {
			memcpy(last_fn_a, a_fn, MAX_FILENAME);
			if (ta->bpm > 0.0f)
				disp_send_bpm(g_disp_a, &g_disp_seq_a,
					      DISP_DEV_A, ta->bpm * ta->pitch);
			disp_send_waveform(g_disp_a, DISP_DEV_A, ta);
		}

		/* ── Deck B waveform load ── */
		pthread_mutex_lock(&tb->lock);
		int b_loaded = tb->loaded;
		char b_fn[MAX_FILENAME];
		memcpy(b_fn, tb->filename, MAX_FILENAME);
		pthread_mutex_unlock(&tb->lock);

		if (g_disp_b && b_loaded && strcmp(b_fn, last_fn_b) != 0) {
			memcpy(last_fn_b, b_fn, MAX_FILENAME);
			if (tb->bpm > 0.0f)
				disp_send_bpm(g_disp_b, &g_disp_seq_b,
					      DISP_DEV_B, tb->bpm * tb->pitch);
			disp_send_waveform(g_disp_b, DISP_DEV_B, tb);
		}

		/* ── Position stream ── */
		if (ta->loaded && ta->bpm > 0.0f) {
			float beats_per_frame =
				ta->bpm * ta->pitch / (60.0f * 20.0f);
			beat_ctr_a +=
				(uint32_t)(beats_per_frame * 128.0f + 0.5f);
			beat_ctr_a &= 0x3FFFu;
		}
		if (tb->loaded && tb->bpm > 0.0f) {
			float beats_per_frame =
				tb->bpm * tb->pitch / (60.0f * 20.0f);
			beat_ctr_b +=
				(uint32_t)(beats_per_frame * 128.0f + 0.5f);
			beat_ctr_b &= 0x3FFFu;
		}

		if (ta->loaded && ta->playing)
			frame_addr = (uint16_t)((ta->pos /
						 (g_actual_sample_rate / 20)) &
						0x3FFFu);

		if (g_disp_a && a_loaded)
			disp_send_position(g_disp_a, &g_disp_seq_a, DISP_DEV_A,
					   ta, beat_ctr_a, frame_addr);

		uint32_t b_frame_addr = 0;
		if (tb->loaded && tb->playing)
			b_frame_addr =
				(uint16_t)((tb->pos /
					    (g_actual_sample_rate / 20)) &
					   0x3FFFu);

		if (g_disp_b && b_loaded)
			disp_send_position(g_disp_b, &g_disp_seq_b, DISP_DEV_B,
					   tb, beat_ctr_b,
					   (uint16_t)b_frame_addr);

		pthread_mutex_unlock(&g_disp_mutex);
	}

	return NULL;
}

