/* ══════════════════════════════════════════════════════════════════════════
   NS7III DISPLAY SUBSYSTEM
   ══════════════════════════════════════════════════════════════════════════

   Protocol summary (from pcap analysis):
     SysEx frame: F0 47 <dev_id> 37 05 <cmd> <len_hi> <len_lo>
                  <exp_hi> <exp_lo> <seq_hi> <seq_lo> <payload...> F7 F7

   dev_id: 0x20 = Deck A (Display Right ALSA card — left platter)
           0x10 = Deck B (Display Left  ALSA card — right platter)
   The displays are "crossed": the right ALSA card faces the left platter and
   vice versa.  Confirmed by cold-init power-cycle pcap of Serato.
   All length/sequence fields are 14-bit (7-bit MSB + 7-bit LSB).

   Commands used by djcmd:
     cmd=0x06  Display init A  (sent once on connect, 10-byte payload)
     cmd=0x08  Display init B  (sent once on connect, 10-byte payload)
     cmd=0x24  Waveform column (411 bytes: [0x01][136 RGB px × 3][0x7F 0x0F])
     cmd=0x05  Beat/position   (16-byte payload, sent at ~20Hz while playing)

   Color encoding: RGB channels 0–127 each (7-bit MIDI safe).
   Scale by 2 (or map 0–127 → 0–255) to get display brightness.

   ══════════════════════════════════════════════════════════════════════════ */

